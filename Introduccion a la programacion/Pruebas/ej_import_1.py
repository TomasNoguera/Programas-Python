def perimetro_cuadrado(lado):
    """
    Devuelve el lado de un cuadrado.
    """
    perimetro = 0
    perimetro = 4 * lado
    return perimetro

def perimetro_rectangulo(base, altura):
    """
    Devuelve el perimetro de un rectangulo
    """
    perimetro = 0
    perimetro = 2 * base + 2 * altura
    return perimetro
