def suma(a, b):
    """
    Suma aritmetica de dos numeros ingresados y retorne (return) el resultado
    """
    resultado = a + b
    return resultado

numero1 = int(input("Ingrese el primero número que desee calcular: "))
numero2 = int(input("ingrese el segundo número que desee calcular: "))
print(suma(numero1,numero2))
