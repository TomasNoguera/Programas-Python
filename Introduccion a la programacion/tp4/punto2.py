"""
lee dos números enteros por teclado, y luego muestra en pantalla el resultado de la suma entre ellos.
"""
primernumero = int(input(f"Ingrese el primer numero que desee calcular: "))
segundonumero = int(input(f"Ingrese el segundo Número que desee calcular: "))
resultado = primernumero + segundonumero
print(f"El Resultado de la suma de {primernumero}+{segundonumero} es: {resultado} ")

